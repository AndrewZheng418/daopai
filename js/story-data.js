function getTerm(key) {
  return XIANXIA_TERMS[key] || key;
}

function formatCard(card, showPoker = true) {
  const info = SUIT_NAMES[card.suit] || { name: card.suit, color: '#000' };
  const rName = RANK_NAMES[card.rank] || card.rank;
  if (!showPoker) {
    return `<span style="color:${info.color}">${info.name}·${rName}</span>`;
  }
  return `<span style="color:${info.color}">${info.name}·${rName}</span><span class="card-poker">(${card.suit}${card.rank})</span>`;
}

function formatCards(cards, showPoker = true) {
  return cards.map(c => formatCard(c, showPoker)).join(' ');
}

